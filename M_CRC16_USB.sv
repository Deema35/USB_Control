
module M_CRC16_USB

(
	input wire clk,
	input wire rst,
	
	input wire Enable,
	input wire GetData,
	input wire [7:0]  Data,
	
	output reg Valid,
	
	output wire [15:0]CRC
	


);

assign Valid = (State == S_IDLE);

assign CRC = CRC_Temp ^ 16'hFFFF;

reg  [15:0] Index = 'd0;
wire [15:0] ROM_Data;

M_CRC16_ROM CRC_Table
(
	.Index_0(Index),
	.ROM_Data_0(ROM_Data)
);


reg [7:0] State = 'd0;
reg [15:0] CRC_Temp = 'hffff;
reg [15:0]CRC_Buf = 'd0;


localparam 	S_IDLE = 'd0,
				S_BIT_SHIFT = 'd1,
				S_INDEX_CALK = 'd2,
				S_CRC_CALK = 'd3;
				


always @(posedge clk) 
begin
	if (rst)
	begin
		CRC_Temp <= 'hffff;
		CRC_Buf <= 'd0;
		Index <= 'd0;
		
		State <= S_IDLE;
	end
	else
	begin
		case(State)
		
		S_IDLE: 
		begin
			if (Enable)
			begin
				if (GetData) 
					State <= S_BIT_SHIFT;
			end
			else
			begin
				CRC_Temp <= 'hffff;
				CRC_Buf <= 'd0;
				Index <= 'd0;
			end
		end
			
			
		S_BIT_SHIFT: 
		begin
			CRC_Buf <= CRC_Temp >> 8;
			CRC_Temp <= CRC_Temp & 16'hFF;
			State <= S_INDEX_CALK;
		end
		
		S_INDEX_CALK: 
		begin
			Index <= CRC_Temp ^ Data;
			State <= S_CRC_CALK;
		end
		
		S_CRC_CALK:
		begin
			CRC_Temp <= ROM_Data ^ CRC_Buf;
			State <= S_IDLE;
		end
		
		
			
			
		endcase
	end
	
	
	
end

endmodule

